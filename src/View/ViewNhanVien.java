    void FillTabel_NV1(List<NhanVien>List){
        mol = (DefaultTableModel) tblDangLam_NV.getModel();
        mol.setRowCount(0);
        for (NhanVien nhanVien : List) {
            mol.addRow(nhanVien.toDaTaRowNV());
        }
    }
